export const CHAR_WIDTH = 5;
export const CHAR_HEIGHT = 7;
export const CHAR_SPACING = 1;
export const GRID_WEEKS = 52;
export const GRID_DAYS = 7;
export const GRID_LENGTH = GRID_WEEKS * GRID_DAYS;

function decode(rows: string[]): number[][] {
  return rows.map((row) =>
    row.split("").map((ch) => (ch === "#" ? 1 : 0))
  );
}

export const FONT_MATRIX: Record<string, number[][]> = Object.fromEntries(
  Object.entries(RAW_FONT).map(([key, rows]) => [key, decode(rows)])
);

export function renderTextToGrid(
  text: string,
  intensity: number,
  gridArray: number[]
): number[] {
  const upper = text.toUpperCase();
  const chars = Array.from(upper).filter((c) => FONT_MATRIX[c] !== undefined);
  if (chars.length === 0) return gridArray;

  const totalWidth =
    chars.length * CHAR_WIDTH + (chars.length - 1) * CHAR_SPACING;

  if (totalWidth > GRID_WEEKS) {
    const maxChars = Math.floor(
      (GRID_WEEKS + CHAR_SPACING) / (CHAR_WIDTH + CHAR_SPACING)
    );
    chars.length = Math.max(0, maxChars);
  }

  const fittedWidth =
    chars.length * CHAR_WIDTH + (chars.length - 1) * CHAR_SPACING;
  const startCol = Math.max(0, Math.floor((GRID_WEEKS - fittedWidth) / 2));
  const startRow = Math.max(0, Math.floor((GRID_DAYS - CHAR_HEIGHT) / 2));

  for (let p = 0; p < chars.length; p++) {
    const matrix = FONT_MATRIX[chars[p]];
    const charCol = startCol + p * (CHAR_WIDTH + CHAR_SPACING);
    for (let r = 0; r < CHAR_HEIGHT; r++) {
      for (let c = 0; c < CHAR_WIDTH; c++) {
        if (matrix[r][c] === 1) {
          const col = charCol + c;
          const row = startRow + r;
          if (col >= 0 && col < GRID_WEEKS && row >= 0 && row < GRID_DAYS) {
            gridArray[col * GRID_DAYS + row] = intensity;
          }
        }
      }
    }
  }

  return gridArray;
}
